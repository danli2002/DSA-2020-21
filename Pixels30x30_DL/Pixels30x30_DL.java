/*
* Write a program, Pixels30x30_YI.java to display the R   G   B values of a given image.
*
* In your project, include the Picture.java (Links to an external site.) file from the StdLib page.
* In the same project, include the image below, Candy Land.
* The program Pixels30x30_YI.java displays the R   G   B values of the upper right corner of 30x30 pixels from the image.
*
* Daniel Li
*
* Java 1.8.0
*/

import java.awt.Color;

public class Pixels30x30_DL {
    public static void main (String[] args) {
        // Initializes the picture
        Picture picture = new Picture("Candy-Land.jpg");
        // Creates nice formatted column headers
        System.out.println(String.format("%-5s %-5s %-5s","R","G","B"));
        // Loops through pixels in top left 30x30 region and spits out RGB components
        for(int i = 0; i < 30; i++) {
            for(int j = 0; j < 30; j++) {
                Color c = new Color(picture.getRGB(i,j));
                int r = c.getRed();
                int g = c.getGreen();
                int b = c.getBlue();
                // Spit out in nice format
                System.out.println(String.format("%-5s %-5s %-5s",r,g,b));
            }
        }
    }
}

